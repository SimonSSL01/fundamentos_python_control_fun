n = 10
suma = 0
for i in range(1, n+1):
    suma += i
print(f"La suma de los primeros {n} números es: {suma}")  # 55

# Este código calcula la suma de los primeros n números enteros utilizando un bucle for. La variable "suma" se inicializa en 0, y luego el bucle itera desde 1 hasta n (inclusive), agregando cada número a la variable "suma". Al final, se imprime el resultado. En este caso, para n=10, la suma será 55.