for numero in range(1, 11):
    if numero == 5:
        print("¡Encontrado el 5! Saliendo del bucle...")
        break
    print(f"Número actual: {numero}")

print("Bucle terminado")

# Este código utiliza un bucle for para iterar a través de los números del 1 al 10. Dentro del bucle, se verifica si el número actual es igual a 5. Si es así, se imprime un mensaje indicando que se ha encontrado el número 5 y se utiliza la declaración break para salir del bucle inmediatamente. Si el número no es 5, se imprime el número actual. Al finalizar el bucle, se imprime un mensaje indicando que el bucle ha terminado. En este caso, el programa imprimirá los números del 1 al 4, luego el mensaje de que se ha encontrado el 5, y finalmente el mensaje de que el bucle ha terminado.