color = "azul"

if color == "rojo":
    print("El color es rojo.")
elif color == "verde":
    print("El color es verde.")
elif color == "azul":
    print("El color es azul.")

# Este código verifica el valor de la variable 'color' y compara si es igual a "rojo", "verde" o "azul". Dependiendo del resultado, imprime un mensaje indicando el color correspondiente. En este caso, como 'color' es igual a "azul", se imprimirá "El color es azul."