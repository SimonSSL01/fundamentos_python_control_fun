edad = 25
ingresos = 30000

if edad >= 18 and ingresos >= 20000:
    print("Eres elegible para el crédito.")

# Este código verifica si la variable 'edad' es mayor o igual a 18 y si la variable 'ingresos' es mayor o igual a 20000. Si ambas condiciones se cumplen, se imprime un mensaje indicando que la persona es elegible para el crédito. En este caso, como 'edad' es igual a 25 y 'ingresos' es igual a 30000, se imprimirá "Eres elegible para el crédito."