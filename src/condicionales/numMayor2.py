a = 1

b = 2

print("El máximo es:", a if a > b else b)

# Este código compara dos números, 'a' y 'b', para determinar cuál es el mayor. Utiliza una expresión condicional (operador ternario) para asignar el valor de 'a' a la función 'print' si 'a' es mayor que 'b', o el valor de 'b' si 'b' es mayor o igual a 'a'. En este caso, como 'b' es mayor que 'a', se imprimirá "El máximo es: 2".