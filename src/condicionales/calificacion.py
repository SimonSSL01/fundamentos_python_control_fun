nota = 87

if nota >= 90:
    print("Calificación: Sobresaliente")
elif nota >= 80:
    print("Calificación: Notable")
elif nota >= 70:
    print("Calificación: Aprobado")
else:
    print("Calificación: Suspenso")

# Este código verifica la variable 'nota' y asigna una calificación basada en su valor. Si la nota es 90 o más, se imprime "Calificación: Sobresaliente". Si la nota es entre 80 y 89, se imprime "Calificación: Notable". Si la nota es entre 70 y 79, se imprime "Calificación: Aprobado". Si la nota es menor que 70, se imprime "Calificación: Suspenso".