dividendo = 10
divisor = 0

if divisor != 0 and dividendo / divisor > 1:
    print("El resultado de la división es mayor que 1.")
else:
    print("No es posible dividir entre cero.")

# Este código define dos variables, 'dividendo' y 'divisor', con los valores 10 y 0 respectivamente. Luego, utiliza una estructura de control 'if' para verificar si el 'divisor' es diferente de 0 antes de realizar la división. Si el 'divisor' no es 0, se realiza la división y se verifica si el resultado es mayor que 1. Si ambas condiciones se cumplen, se imprime "El resultado de la división es mayor que 1." Si el 'divisor' es 0, se imprime "No es posible dividir entre cero." En este caso, como el divisor es 0, se imprimirá "No es posible dividir entre cero."    