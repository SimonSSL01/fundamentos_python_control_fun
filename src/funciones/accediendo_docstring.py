# Una característica poderosa de los docstrings 
# es que están disponibles 
# en tiempo de ejecución a través del atributo __doc__ o la función help():

# Acceder al docstring directamente
print(calcular_promedio.__doc__)

# O usar la función help
help(calcular_promedio)

"""Esto permite que herramientas como el intérprete interactivo de Python, 
editores de código y generadores 
de documentación automática puedan mostrar esta información al programador."""

